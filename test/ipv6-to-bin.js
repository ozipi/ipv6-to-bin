var should = require('chai').should(),
    ip2bin = require('../src/ipv6-to-bin.js');

describe('#ipv6-to-bin', function() {
  it('converts 0:0:0:0:0:0:0:1 into 00000000:00000000:00000000:00000000:00000000:00000000:00000000:00000001;', function() {
    ip2bin('0:0:0:0:0:0:0:1').should.equal('00000000:00000000:00000000:00000000:00000000:00000000:00000000:00000001');
  });

  it('converts 2001:4860:4860::8888 into 00000000:00000000:00000000:00000000:00000000:00000000:00000000:00000001;', function() {
    ip2bin('2001:4860:4860::8888').should.equal('00000000:00000000:00000000:00000000:00000000:00000000:00000000:00000001');
  });
});